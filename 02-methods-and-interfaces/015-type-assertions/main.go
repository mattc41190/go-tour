package main

import "fmt"

func main() {
	var i interface{} = "hello"

	s := i.(string)
	fmt.Println(s)

	s, ok := i.(string)
	fmt.Println(s, ok)

	f, ok := i.(float64)
	fmt.Println(f, ok)

	// f := i.(float64) // <-- uncomment line to see the panic that occurs when an invalid type assertion occurs and a checker is absent
}
